#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>

int main()
{
	int t, d, w, c, k;
	int arvore, peso, qualidade;
	
	scanf("%d", &k);
	while(k-- > 0)
	{
		scanf("%d %d %d %d", &t, &d, &w, &c);
		while(c-- > 0)
		{
			scanf("%d %d %d", arvore, peso, qualidade);
			
		}
	}
	
	return 0;
}
